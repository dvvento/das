let calen = prompt('напиши число недели')

switch (calen) {
    case '1' :
        alert('понидельник')
        break;
    case '2' :
        alert('вторник')
        break;
    case '3' :
        alert('среда')
        break;
    case '4' :
        alert('четверг')
        break;
    case '5' :
        alert('пятница')
        break;
    case '6' :
        alert('суботта')
        break;
    case '7' :
        alert('восресенье')
        break;
    default:
        alert('напиши число недели!')
        break;
}